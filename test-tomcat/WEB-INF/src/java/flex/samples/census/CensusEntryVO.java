package flex.samples.census;

public class CensusEntryVO {

	private int id;

	private int age;

	private String classOfWorker;

	private String education;

	private String maritalStatus;

	private String race;

	private String sex;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getClassOfWorker() {
		return classOfWorker;
	}

	public void setClassOfWorker(String classOfWorker) {
		this.classOfWorker = classOfWorker;
	}

	public String getEducation() {
		return education;
	}

	public void setEducation(String education) {
		this.education = education;
	}

	public String getMaritalStatus() {
		return maritalStatus;
	}

	public void setMaritalStatus(String maritalStatus) {
		this.maritalStatus = maritalStatus;
	}

	public String getRace() {
		return race;
	}

	public void setRace(String race) {
		this.race = race;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

}
